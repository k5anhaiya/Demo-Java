
public class printsystem 
{
	public static void main(String[] args) 
	{
		int num1=90,num2=100;
		
		System.out.println(num1);  // for printing anything in vertical we use println command
		System.out.println(",");
		System.out.println(num2);
		System.out.print(num1); // for printing anything in horizontal we use print command
		System.out.print(","); 
		System.out.print(num2);

	}

}
