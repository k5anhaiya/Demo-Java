public class relational_operatortype2 
{
	public static void main(String[] args) 
	{
		int x=10,y=10;
	
	                //use of || i.e or //relational operator
	boolean z=++x<10 || ++y>10; //here it will check for the first condition if first
    //	condition will false then it will check the second condition ALSO if the first condition will
	//	true then it will not check for the second condition.
	//          f        t
	System.out.println(x);
	System.out.println(y);
	System.out.println(z);
	}

}
